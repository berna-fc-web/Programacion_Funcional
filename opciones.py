def cuadrado(x:int): return x*x
def multiplicacion(x:int, y:int): return x*y
def suma(x:int,y:int): return x+y
def resta(x:int, y:int): return x-y
def division(x:int, y:int): return x/y

if __name__ == "__main__":
    print(suma(5,6))
    print(resta(5,6))
    print(multiplicacion(5,6))
    print(division(5,6))
    print(cuadrado(5))